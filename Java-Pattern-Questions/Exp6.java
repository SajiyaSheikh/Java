public class Exp6 {

    public static void main(String []args)
    {
        // Number Triangle Pattern

        for(int i=0;i<4;i++)
        {
            for(int j=0;j<i+1;j++)
            {
                System.out.print(j+1 + " ");
            }
 
            System.out.println();
        }
    }
    
}
