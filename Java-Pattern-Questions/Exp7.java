public class Exp7 {

    public static void main(String []args)
    {
        // Repeating Number Triangle Pattern

        for(int i=0;i<4;i++)
        {
            for(int j=0;j<=i;j++)
            {
                System.out.print(i+1+" ");
            }

            System.out.println();
        }
    }
    
}
